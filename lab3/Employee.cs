public class Employee {
    public string name;
    public int age;
    public bool gender;
    public int costPerHour;
    public int id;

    public Employee(string n, int a, bool g, int cph, int i)
    {
        name = n;
        age = a;
        gender = g;
        costPerHour = cph;
        id = i;
    }
}